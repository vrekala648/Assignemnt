# I imported the csv module so that I could efficiently read and process CSV formatted dataset files.
import csv

# I defined a class to handle dataset loading and preprocessing tasks.
class DatasetLoader:

    # I defined the constructor to initialize the DatasetLoader object.
    # I accepted the dataset file path as an input parameter to allow flexibility in loading different datasets.
    def __init__(self, file_location):

        # I stored the file path as an instance variable so that it could be accessed later within other methods.
        self.file_location = file_location

    # I defined a method to load, clean, and transform the dataset into a structured format.
    # I designed this method to return a list of dictionaries where each dictionary represents one processed record.
    def read_dataset(self):

        # I initialized an empty list to store all processed dataset records.
        # I used a list because it preserves order and allows easy iteration over records.
        records_collection = []

        # I used a try block to handle potential runtime errors such as file access issues.
        try:

            # I opened the dataset file in read mode.
            # I specified UTF-8 encoding to handle special characters correctly.
            # I used the 'with' statement to ensure the file was automatically closed after processing.
            with open(self.file_location, mode='r', encoding='utf-8') as dataset_file:

                # I created a DictReader object to read each row as a dictionary.
                # I ensured that column headers were used as keys and row values as dictionary values.
                csv_reader_object = csv.DictReader(dataset_file)

                # I iterated through each row in the dataset.
                for single_entry in csv_reader_object:

                    # I initialized a new dictionary to store cleaned and structured data for the current row.
                    # I avoided modifying raw data directly to maintain data integrity.
                    processed_entry = {}

                    # I looped through each key-value pair in the row.
                    for attribute, value in single_entry.items():

                        # I standardized column names by removing spaces and converting to lowercase.
                        # I replaced spaces with underscores to ensure consistency in key naming.
                        clean_key = attribute.strip().lower().replace(" ", "_")

                        # I checked whether the value was missing (None or empty string).
                        # I treated such cases as missing data.
                        if value is None or value.strip() == "":

                            # I assigned None to represent missing values in a standardized way.
                            processed_entry[clean_key] = None

                            # I skipped further processing for this field.
                            continue

                        # I removed unnecessary whitespace from the value.
                        # I ensured that the data was clean before processing.
                        clean_value = value.strip()

                        # I attempted to convert numeric values into appropriate data types.
                        try:

                            # I checked whether the value contained a decimal point.
                            # I assumed such values should be converted to float.
                            if "." in clean_value:

                                # I converted the value to float for precise numerical representation.
                                processed_entry[clean_key] = float(clean_value)

                            else:
                                # I handled values without decimal points as integers.

                                # I converted the value to integer for efficient computation.
                                processed_entry[clean_key] = int(clean_value)

                        # I handled cases where numeric conversion failed.
                        except ValueError:

                            # I converted string values to lowercase for consistency in categorical data.
                            processed_entry[clean_key] = clean_value.lower()

                    # I appended the processed record to the main collection.
                    records_collection.append(processed_entry)

            # I returned the fully processed dataset after reading all rows.
            return records_collection

        # I handled the case where the dataset file was not found.
        except FileNotFoundError:

            # I displayed a clear error message for the user.
            print("Error: Dataset file was not found.")

            # I returned an empty list to maintain consistency and avoid crashes.
            return []

        # I handled any unexpected errors during dataset processing.
        except Exception as error_message:

            # I printed the error message to assist with debugging.
            print("Error while reading dataset:", error_message)

            # I returned an empty list to ensure consistent output type.
            return []